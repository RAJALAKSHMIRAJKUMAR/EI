		FROM ',DESTINATIONSCHEMA,'.UNIT U INNER JOIN ',SOURCESCHEMA,'.BIZ_DAILY_SCDB_FORMAT BSF, ',DESTINATIONSCHEMA,'.USER_LOGIN_DETAILS ULD WHERE 
