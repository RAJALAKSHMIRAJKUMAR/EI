		EDV_COMMENTS TEXT NULL,	
		ULD_ID INTEGER(2) NOT NULL,	
		EDV_TIMESTAMP TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
