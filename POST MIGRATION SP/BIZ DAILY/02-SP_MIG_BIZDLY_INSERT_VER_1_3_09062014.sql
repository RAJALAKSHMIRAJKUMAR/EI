		EXECUTE COUNT_SCDBECP;    
		SET @COUNT_SPLITTING_ECP=(SELECT CONCAT('SELECT COUNT(*)INTO @SPLITTED_CARPARK FROM ',DESTINATIONSCHEMA,'.EXPENSE_CARPARK'));
