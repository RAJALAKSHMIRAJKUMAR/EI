		EXECUTE EFUDROPQUERY;	
		SET @EFU_CREATEQUERY=(SELECT CONCAT('CREATE TABLE ',DESTINATIONSCHEMA,'.EXPENSE_FACILITY_USE(
		EFU_ID	INTEGER NOT NULL AUTO_INCREMENT,
