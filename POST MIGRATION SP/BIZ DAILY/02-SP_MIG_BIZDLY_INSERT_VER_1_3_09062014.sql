		SELECT EDCP.EDCP_ID, BSF.EXP_CAR_INVOICE_DATE, BSF.EXP_CAR_START_PERIOD, BSF.EXP_CAR_END_PERIOD, BSF.EXP_CAR_AMOUNT, BSF.EXP_CAR_COMMENTS, ULD.ULD_ID,BSF.TIMESTAMP
