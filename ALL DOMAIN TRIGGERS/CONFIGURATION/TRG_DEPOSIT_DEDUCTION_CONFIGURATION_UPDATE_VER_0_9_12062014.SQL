-- after update on DEPOSIT_DEDUCTION_CONFIGURATION table this trigger will be executed
DROP TRIGGER IF EXISTS TRG_DEPOSIT_DEDUCTION_CONFIGURATION_UPDATE;
CREATE TRIGGER TRG_DEPOSIT_DEDUCTION_CONFIGURATION_UPDATE  
AFTER UPDATE ON DEPOSIT_DEDUCTION_CONFIGURATION
FOR EACH ROW
BEGIN 
-- declaration for old_value and new_value to store it in TICKLER_HISTORY table
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.DDC_DATA!= NEW.DDC_DATA) OR (OLD.DDC_SUB_DATA IS NULL AND NEW.DDC_SUB_DATA IS NOT NULL) OR (OLD.DDC_SUB_DATA IS NOT NULL AND NEW.DDC_SUB_DATA IS NULL) OR (OLD.DDC_SUB_DATA!= NEW.DDC_SUB_DATA) OR
(OLD.DDC_INITIALIZE_FLAG IS NULL AND NEW.DDC_INITIALIZE_FLAG IS NOT NULL) OR (OLD.DDC_INITIALIZE_FLAG IS NOT NULL AND NEW.DDC_INITIALIZE_FLAG IS NULL) OR (OLD.DDC_INITIALIZE_FLAG!= NEW.DDC_INITIALIZE_FLAG) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'DDC_ID=', OLD.DDC_ID,','); 
END IF;
-- get the OLD VALUE & NEW_VALUE for CGN_ID to store it in TICKLER_HISTORY table
IF (OLD.CGN_ID!= NEW.CGN_ID) THEN  
 SET OLD_VALUE = CONCAT(OLD_VALUE,'CGN_ID=', OLD.CGN_ID,','); 
 SET NEW_VALUE = CONCAT(NEW_VALUE,'CGN_ID=', NEW.CGN_ID,','); 
END IF;

-- get the OLD VALUE & NEW_VALUE for DDC_DATA to store it in TICKLER_HISTORY table
IF (OLD.DDC_DATA!= NEW.DDC_DATA) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'DDC_DATA=', OLD.DDC_DATA,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'DDC_DATA=', NEW.DDC_DATA,','); 
END IF;

-- get the OLD VALUE & NEW_VALUE for DDC_SUB_DATA to store it in TICKLER_HISTORY table
IF (OLD.DDC_SUB_DATA IS NULL AND NEW.DDC_SUB_DATA IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'DDC_SUB_DATA=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'DDC_SUB_DATA=',NEW.DDC_SUB_DATA,',');
ELSEIF (OLD.DDC_SUB_DATA IS NOT NULL AND NEW.DDC_SUB_DATA IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'DDC_SUB_DATA=',OLD.DDC_SUB_DATA,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'DDC_SUB_DATA=','<NULL>,');
ELSEIF (OLD.DDC_SUB_DATA!= NEW.DDC_SUB_DATA) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'DDC_SUB_DATA=', OLD.DDC_SUB_DATA,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'DDC_SUB_DATA=', NEW.DDC_SUB_DATA,','); 
END IF;
-- get the OLD VALUE & NEW_VALUE for DDC_INITIALIZE_FLAG to store it in TICKLER_HISTORY table
IF (OLD.DDC_INITIALIZE_FLAG IS NULL AND NEW.DDC_INITIALIZE_FLAG IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'DDC_INITIALIZE_FLAG=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'DDC_INITIALIZE_FLAG=',NEW.DDC_INITIALIZE_FLAG,',');
ELSEIF (OLD.DDC_INITIALIZE_FLAG IS NOT NULL AND NEW.DDC_INITIALIZE_FLAG IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'DDC_INITIALIZE_FLAG=',OLD.DDC_INITIALIZE_FLAG,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'DDC_INITIALIZE_FLAG=','<NULL>,');
ELSEIF (OLD.DDC_INITIALIZE_FLAG!= NEW.DDC_INITIALIZE_FLAG) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'DDC_INITIALIZE_FLAG=', OLD.DDC_INITIALIZE_FLAG,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'DDC_INITIALIZE_FLAG=', NEW.DDC_INITIALIZE_FLAG,','); 
END IF;
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.DDC_DATA!= NEW.DDC_DATA) OR (OLD.DDC_SUB_DATA IS NULL AND NEW.DDC_SUB_DATA IS NOT NULL) OR (OLD.DDC_SUB_DATA IS NOT NULL AND NEW.DDC_SUB_DATA IS NULL) OR (OLD.DDC_SUB_DATA!= NEW.DDC_SUB_DATA) OR
(OLD.DDC_INITIALIZE_FLAG IS NULL AND NEW.DDC_INITIALIZE_FLAG IS NOT NULL) OR (OLD.DDC_INITIALIZE_FLAG IS NOT NULL AND NEW.DDC_INITIALIZE_FLAG IS NULL) OR (OLD.DDC_INITIALIZE_FLAG!= NEW.DDC_INITIALIZE_FLAG) THEN 
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.DDC_TIMESTAMP!= NEW.DDC_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'DDC_TIMESTAMP=', OLD.DDC_TIMESTAMP,','); 
END IF;
END IF;
-- inserting old_values and new_values in the TICKLER_HISTORY table with their corresponding TTIP_ID in TICKLER_TABID_PROFILE
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF (OLD_VALUE!=NEW_VALUE)THEN

-- REMOVING COMMA(,) AT THE END OF OLD_VALUE & NEW_VALUE
     SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
     SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);

INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM DEPOSIT_DEDUCTION_CONFIGURATION WHERE DDC_ID=NEW.DDC_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='DEPOSIT_DEDUCTION_CONFIGURATION'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;

