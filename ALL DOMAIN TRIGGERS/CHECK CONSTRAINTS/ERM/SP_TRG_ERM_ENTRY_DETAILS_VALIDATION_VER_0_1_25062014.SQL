DROP PROCEDURE IF EXISTS SP_TRG_ERM_ENTRY_DETAILS_VALIDATION;
CREATE PROCEDURE SP_TRG_ERM_ENTRY_DETAILS_VALIDATION(
IN NEWNCID INTEGER,
IN PROCESS TEXT)
BEGIN
	DECLARE MESSAGE_TEXT VARCHAR(50);
	IF(PROCESS = 'INSERT') OR (PROCESS = 'UPDATE') THEN
		IF(NEWNCID IS NOT NULL)THEN
			IF NOT EXISTS(SELECT NC_ID FROM NATIONALITY_CONFIGURATION WHERE NC_ID = NEWNCID AND CGN_ID = 15)THEN
				SIGNAL SQLSTATE '45000'
				SET MESSAGE_TEXT = ' NC_ID NOT IN NATIONALITY CONFIGURATION TABLE';
			END IF;
		END IF;
	END IF;
END;