DROP TRIGGER IF EXISTS TRG_UNIT_DETAILS_UPDATE_VALIDATION;
CREATE TRIGGER TRG_UNIT_DETAILS_UPDATE_VALIDATION
BEFORE UPDATE ON UNIT_DETAILS
FOR EACH ROW
BEGIN
	CALL SP_TRG_UNIT_DETAILS_VALIDATION(NEW.UD_START_DATE,NEW.UD_END_DATE,'UPDATE');
END;