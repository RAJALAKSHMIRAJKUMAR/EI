DROP PROCEDURE IF EXISTS SP_TRG_UNIT_DETAILS_VALIDATION;
CREATE PROCEDURE SP_TRG_UNIT_DETAILS_VALIDATION(
IN STARTDATE DATE,
IN ENDATE DATE,
IN PROCESS TEXT)
BEGIN
	DECLARE MESSAGE_TEXT VARCHAR(50);
	IF(PROCESS = 'INSERT') OR (PROCESS = 'UPDATE') THEN
		IF(STARTDATE > ENDATE) THEN
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = 'UD_START_DATE LESS THAN OR EQUAL TO UNIT ENDDATE';
		END IF;
	END IF;
END;