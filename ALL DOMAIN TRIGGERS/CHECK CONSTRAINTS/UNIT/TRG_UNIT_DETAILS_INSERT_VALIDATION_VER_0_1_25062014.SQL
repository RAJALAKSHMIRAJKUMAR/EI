DROP TRIGGER IF EXISTS TRG_UNIT_DETAILS_INSERT_VALIDATION;
CREATE TRIGGER TRG_UNIT_DETAILS_INSERT_VALIDATION
BEFORE INSERT ON UNIT_DETAILS
FOR EACH ROW
BEGIN
	CALL SP_TRG_UNIT_DETAILS_VALIDATION(NEW.UD_START_DATE,NEW.UD_END_DATE,'INSERT');
END;