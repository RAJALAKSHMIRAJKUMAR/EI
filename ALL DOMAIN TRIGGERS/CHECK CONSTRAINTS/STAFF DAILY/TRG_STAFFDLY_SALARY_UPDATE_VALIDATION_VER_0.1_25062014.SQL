DROP TRIGGER IF EXISTS TRG_STAFFDLY_SALARY_UPDATE_VALIDATION;
CREATE TRIGGER TRG_STAFFDLY_SALARY_UPDATE_VALIDATION
BEFORE UPDATE ON EXPENSE_STAFF_SALARY
FOR EACH ROW
BEGIN
	CALL SP_TRG_STAFFDLY_SALARY_VALIDATION(OLD.ESS_ID,NULL,NEW.ESS_INVOICE_DATE,NEW.ESS_FROM_PERIOD,NEW.ESS_TO_PERIOD,'UPDATE');
END;