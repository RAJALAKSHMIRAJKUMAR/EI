--after update on EMAIL_LIST  table this trigger will be executed
DROP TRIGGER IF EXISTS TRG_BIZDTL_AIRCON_SERVICE_BY_UPDATE;
CREATE TRIGGER TRG_BIZDTL_AIRCON_SERVICE_BY_UPDATE  
AFTER UPDATE ON EXPENSE_AIRCON_SERVICE_BY 
FOR EACH ROW
BEGIN 
-- declaration for old_value and new_value to store it in TICKLER_HISTORY table
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.EASB_DATA!= NEW.EASB_DATA) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'EASB_ID=', OLD.EASB_ID,','); 
END IF;
-- get the OLD VALUE & NEW_VALUE for EASB_DATA to store it in TICKLER_HISTORY table
IF (OLD.EASB_DATA!= NEW.EASB_DATA) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EASB_DATA=', OLD.EASB_DATA,','); 
END IF;
IF (OLD.EASB_DATA!= NEW.EASB_DATA) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EASB_DATA=', NEW.EASB_DATA,','); 
END IF;
IF (OLD.EASB_DATA!= NEW.EASB_DATA) THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.EASB_TIMESTAMP!= NEW.EASB_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EASB_TIMESTAMP=', OLD.EASB_TIMESTAMP,','); 
END IF;
END IF;
-- inserting old_values and new_values in the TICKLER_HISTORY table with their corresponding TTIP_ID in TICKLER_TABID_PROFILE
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE != NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);

INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EXPENSE_AIRCON_SERVICE_BY  WHERE EASB_ID=NEW.EASB_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EXPENSE_AIRCON_SERVICE_BY'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;
