DROP PROCEDURE IF EXISTS SP_TRG_OCBC_BANK_RECORDS_VALIDATION;
CREATE PROCEDURE SP_TRG_OCBC_BANK_RECORDS_VALIDATION(
IN ACCOUNTNUMBER INTEGER,
IN CURRENCY INTEGER,
IN OCNID INTEGER,
IN PROCESS TEXT)
BEGIN
	DECLARE MESSAGE_TEXT VARCHAR(50);
	DECLARE ERRORMSG TEXT;
	IF(PROCESS = 'INSERT') OR (PROCESS = 'UPDATE')THEN
		IF(ACCOUNTNUMBER!=3)THEN
    	    SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=508);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT=ERRORMSG;
		END IF;
		IF(CURRENCY!=2)THEN
            SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=509);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT=ERRORMSG;
		END IF;
		IF NOT EXISTS(SELECT OCN_ID FROM OCBC_CONFIGURATION WHERE OCN_ID=OCNID AND CGN_ID=80)THEN
            SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=510);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT=ERRORMSG;
		END IF;
	END IF;
END;
