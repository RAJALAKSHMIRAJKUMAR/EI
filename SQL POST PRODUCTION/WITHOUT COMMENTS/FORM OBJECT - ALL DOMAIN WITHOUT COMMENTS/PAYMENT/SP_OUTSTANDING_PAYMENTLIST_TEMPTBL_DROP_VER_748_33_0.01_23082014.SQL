	PREPARE TEMP_ACTIVE_CUSTOMER_PROCESSFEE_STMT FROM @TEMP_ACTIVE_CUSTOMER_PROCESSFEE_DROPQUERY;
	EXECUTE TEMP_ACTIVE_CUSTOMER_PROCESSFEE_STMT;
