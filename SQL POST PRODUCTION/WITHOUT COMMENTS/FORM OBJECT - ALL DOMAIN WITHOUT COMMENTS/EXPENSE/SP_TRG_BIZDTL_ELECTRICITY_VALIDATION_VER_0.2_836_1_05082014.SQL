DROP PROCEDURE IF EXISTS SP_TRG_BIZDTL_ELECTRICITY_VALIDATION;
CREATE PROCEDURE SP_TRG_BIZDTL_ELECTRICITY_VALIDATION(
IN NEWECNID INTEGER,
IN PROCESS TEXT)
BEGIN
	DECLARE MESSAGE_TEXT VARCHAR(50);
	DECLARE ERRORMSG TEXT;
	IF(PROCESS = 'INSERT') OR (PROCESS = 'UPDATE')THEN
		IF NOT EXISTS(SELECT ECN_ID FROM EXPENSE_CONFIGURATION WHERE ECN_ID = NEWECNID AND CGN_ID = 27)THEN
            SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=517);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERRORMSG;
		END IF;
	END IF;
END; 
