DROP TRIGGER IF EXISTS TRG_BIZDTL_CARPARK_UPDATE;
CREATE TRIGGER TRG_BIZDTL_CARPARK_UPDATE  
AFTER UPDATE ON EXPENSE_DETAIL_CARPARK
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.EDCP_ID!= NEW.EDCP_ID) OR (OLD.UNIT_ID!= NEW.UNIT_ID) OR (OLD.EDCP_REC_VER!=NEW.EDCP_REC_VER) OR (OLD.EDCP_CAR_NO!=NEW.EDCP_CAR_NO) OR (OLD.EDCP_COMMENTS IS NULL AND NEW.EDCP_COMMENTS IS NOT NULL) OR (OLD.EDCP_COMMENTS IS NOT NULL AND NEW.EDCP_COMMENTS IS NULL) OR (OLD.EDCP_COMMENTS != NEW.EDCP_COMMENTS) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'EDCP_ID=', OLD.EDCP_ID,','); 
END IF;
IF (OLD.EDCP_ID!= NEW.EDCP_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDCP_ID=', OLD.EDCP_ID,','); 
END IF;
IF (OLD.EDCP_ID!= NEW.EDCP_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EDCP_ID=', NEW.EDCP_ID,','); 
END IF;
IF (OLD.UNIT_ID!= NEW.UNIT_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'UNIT_ID=', OLD.UNIT_ID,','); 
END IF;
IF (OLD.UNIT_ID!= NEW.UNIT_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'UNIT_ID=', NEW.UNIT_ID,','); 
END IF;
IF (OLD.EDCP_REC_VER!= NEW.EDCP_REC_VER) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDCP_REC_VER=', OLD.EDCP_REC_VER,','); 
END IF;
IF (OLD.EDCP_REC_VER!= NEW.EDCP_REC_VER) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EDCP_REC_VER=', NEW.EDCP_REC_VER,','); 
END IF;
IF (OLD.EDCP_CAR_NO!= NEW.EDCP_CAR_NO) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDCP_CAR_NO=', OLD.EDCP_CAR_NO,','); 
END IF;
IF (OLD.EDCP_CAR_NO!= NEW.EDCP_CAR_NO) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EDCP_CAR_NO=', NEW.EDCP_CAR_NO,','); 
END IF;
IF (OLD.EDCP_COMMENTS IS NULL AND NEW.EDCP_COMMENTS IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EDCP_COMMENTS=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EDCP_COMMENTS=',NEW.EDCP_COMMENTS,',');
ELSEIF(OLD.EDCP_COMMENTS IS NOT NULL AND NEW.EDCP_COMMENTS IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EDCP_COMMENTS=',OLD.EDCP_COMMENTS,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EDCP_COMMENTS=','<NULL>,');
ELSEIF (OLD.EDCP_COMMENTS!= NEW.EDCP_COMMENTS) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDCP_COMMENTS=', OLD.EDCP_COMMENTS,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'EDCP_COMMENTS=', NEW.EDCP_COMMENTS,','); 
END IF;
IF (OLD.EDCP_ID!= NEW.EDCP_ID) OR (OLD.UNIT_ID!= NEW.UNIT_ID)  OR  (OLD.EDCP_REC_VER != NEW.EDCP_REC_VER ) OR  (OLD.EDCP_CAR_NO!=NEW.EDCP_CAR_NO) OR (OLD.EDCP_COMMENTS IS NULL AND NEW.EDCP_COMMENTS IS NOT NULL) OR (OLD.EDCP_COMMENTS IS NOT NULL AND NEW.EDCP_COMMENTS IS NULL) OR (OLD.EDCP_COMMENTS != NEW.EDCP_COMMENTS) THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.EDCP_TIMESTAMP!= NEW.EDCP_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDCP_TIMESTAMP=', OLD.EDCP_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE!=NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EXPENSE_DETAIL_CARPARK WHERE EDCP_ID=NEW.EDCP_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EXPENSE_DETAIL_CARPARK'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;
