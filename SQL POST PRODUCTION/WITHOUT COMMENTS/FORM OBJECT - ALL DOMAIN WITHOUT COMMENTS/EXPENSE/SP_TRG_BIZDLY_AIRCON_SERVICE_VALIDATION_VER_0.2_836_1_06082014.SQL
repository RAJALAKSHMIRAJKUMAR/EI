DROP PROCEDURE IF EXISTS SP_TRG_BIZDLY_AIRCON_SERVICE_VALIDATION;
CREATE PROCEDURE SP_TRG_BIZDLY_AIRCON_SERVICE_VALIDATION(
IN NEWEASDATE DATE,
IN PROCESS TEXT)
BEGIN
	DECLARE MESSAGE_TEXT VARCHAR(50);
  DECLARE ERRORMSG TEXT;
	IF(PROCESS = 'INSERT') OR (PROCESS='UPDATE')THEN
		IF(NEWEASDATE > CURDATE()) THEN
    SET ERRORMSG=(SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID=516);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERRORMSG;
		END IF;
	END IF;
END;
