DROP PROCEDURE IF EXISTS SP_TRG_BIZDLY_PETTYCASH_VALIDATION;
CREATE PROCEDURE SP_TRG_BIZDLY_PETTYCASH_VALIDATION(
IN NEWDATE DATE,
IN CASHIN DECIMAL(6,2),
IN CASHOUT DECIMAL(6,2),
IN PROCESS TEXT)
BEGIN
    DECLARE MESSAGE_TEXT VARCHAR(50);
	DECLARE OLDDATE DATE;
	DECLARE ERROR_MSG TEXT;
	SET OLDDATE = (SELECT MAX(EPC_DATE) FROM EXPENSE_PETTY_CASH);
	IF(PROCESS = 'INSERT') OR (PROCESS = 'UPDATE')THEN
		IF(CASHIN IS NULL AND CASHOUT IS NULL)THEN
			SET ERROR_MSG = (SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID = 536);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERROR_MSG;
		END IF;
		IF(CASHIN IS NOT NULL AND CASHOUT IS NOT NULL)THEN
			SET ERROR_MSG = (SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID = 537);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERROR_MSG;
		END IF;
		IF(NEWDATE NOT BETWEEN OLDDATE AND CURDATE())THEN
			SET ERROR_MSG = (SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID = 538);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERROR_MSG;
		END IF;
	END IF;
END;
