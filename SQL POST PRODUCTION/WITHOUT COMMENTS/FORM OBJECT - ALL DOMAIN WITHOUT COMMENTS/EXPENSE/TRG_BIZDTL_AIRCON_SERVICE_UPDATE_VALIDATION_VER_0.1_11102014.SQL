DROP TRIGGER IF EXISTS TRG_BIZDTL_AIRCON_SERVICE_UPDATE;
CREATE TRIGGER TRG_BIZDTL_AIRCON_SERVICE_UPDATE  
AFTER UPDATE ON EXPENSE_DETAIL_AIRCON_SERVICE
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.EDAS_ID!= NEW.EDAS_ID) OR (OLD.UNIT_ID!= NEW.UNIT_ID) OR (OLD.EASB_ID != NEW.EASB_ID ) OR (OLD.EDAS_REC_VER!=NEW.EDAS_REC_VER) OR (OLD.EDAS_COMMENTS IS NULL AND NEW.EDAS_COMMENTS IS NOT NULL) OR (OLD.EDAS_COMMENTS IS NOT NULL AND NEW.EDAS_COMMENTS IS NULL) OR (OLD.EDAS_COMMENTS != NEW.EDAS_COMMENTS) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'EDAS_ID=', OLD.EDAS_ID,','); 
END IF;
IF (OLD.EDAS_ID!= NEW.EDAS_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDAS_ID=', OLD.EDAS_ID,','); 
END IF;
IF (OLD.EDAS_ID!= NEW.EDAS_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EDAS_ID=', NEW.EDAS_ID,','); 
END IF;
IF (OLD.UNIT_ID!= NEW.UNIT_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'UNIT_ID=', OLD.UNIT_ID,','); 
END IF;
IF (OLD.UNIT_ID!= NEW.UNIT_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'UNIT_ID=', NEW.UNIT_ID,','); 
END IF;
IF (OLD.EASB_ID!= NEW.EASB_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EASB_ID=', OLD.EASB_ID,','); 
END IF;
IF (OLD.EASB_ID!= NEW.EASB_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EASB_ID=', NEW.EASB_ID,','); 
END IF;
IF (OLD.EDAS_REC_VER!= NEW.EDAS_REC_VER) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDAS_REC_VER=', OLD.EDAS_REC_VER,','); 
END IF;
IF (OLD.EDAS_REC_VER!= NEW.EDAS_REC_VER) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EDAS_REC_VER=', NEW.EDAS_REC_VER,','); 
END IF;
IF (OLD.EDAS_COMMENTS IS NULL AND NEW.EDAS_COMMENTS IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EDAS_COMMENTS=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EDAS_COMMENTS=',NEW.EDAS_COMMENTS,',');
ELSEIF(OLD.EDAS_COMMENTS IS NOT NULL AND NEW.EDAS_COMMENTS IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EDAS_COMMENTS=',OLD.EDAS_COMMENTS,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EDAS_COMMENTS=','<NULL>,');
ELSEIF (OLD.EDAS_COMMENTS!= NEW.EDAS_COMMENTS) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDAS_COMMENTS=', OLD.EDAS_COMMENTS,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'EDAS_COMMENTS=', NEW.EDAS_COMMENTS,','); 
END IF;
IF (OLD.EDAS_ID!= NEW.EDAS_ID) OR (OLD.UNIT_ID!= NEW.UNIT_ID)  OR  (OLD.EASB_ID != NEW.EASB_ID ) OR  (OLD.EDAS_REC_VER!=NEW.EDAS_REC_VER) OR (OLD.EDAS_COMMENTS IS NULL AND NEW.EDAS_COMMENTS IS NOT NULL) OR (OLD.EDAS_COMMENTS IS NOT NULL AND NEW.EDAS_COMMENTS IS NULL) OR (OLD.EDAS_COMMENTS != NEW.EDAS_COMMENTS) THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.EDAS_TIMESTAMP!= NEW.EDAS_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDAS_TIMESTAMP=', OLD.EDAS_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE!=NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EXPENSE_DETAIL_AIRCON_SERVICE WHERE EDAS_ID=NEW.EDAS_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EXPENSE_DETAIL_AIRCON_SERVICE'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;
