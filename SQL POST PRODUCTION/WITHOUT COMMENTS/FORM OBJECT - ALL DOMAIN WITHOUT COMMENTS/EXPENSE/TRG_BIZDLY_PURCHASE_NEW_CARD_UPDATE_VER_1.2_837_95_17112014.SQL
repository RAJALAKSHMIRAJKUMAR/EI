DROP TRIGGER IF EXISTS TRG_BIZDLY_PURCHASE_NEW_CARD_UPDATE;
CREATE TRIGGER TRG_BIZDLY_PURCHASE_NEW_CARD_UPDATE  
AFTER UPDATE ON EXPENSE_PURCHASE_NEW_CARD 
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.EPNC_ID!=NEW.EPNC_ID) OR (OLD.UNIT_ID!= NEW.UNIT_ID) OR (OLD.EPNC_NUMBER!= NEW.EPNC_NUMBER) OR (OLD.EPNC_INVOICE_DATE!= NEW.EPNC_INVOICE_DATE) OR  (OLD.EPNC_AMOUNT!= NEW.EPNC_AMOUNT)  OR (OLD.EPNC_COMMENTS IS NULL AND NEW.EPNC_COMMENTS IS NOT NULL) OR (OLD.EPNC_COMMENTS IS NOT NULL AND NEW.EPNC_COMMENTS IS NULL) OR (OLD.EPNC_COMMENTS != NEW.EPNC_COMMENTS) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'EPNC_ID=', OLD.EPNC_ID,','); 
END IF;
IF (OLD.UNIT_ID!= NEW.UNIT_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'UNIT_ID=', OLD.UNIT_ID,','); 
END IF;
IF (OLD.UNIT_ID!= NEW.UNIT_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'UNIT_ID=', NEW.UNIT_ID,','); 
END IF;
IF (OLD.EPNC_NUMBER!= NEW.EPNC_NUMBER) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EPNC_NUMBER=', OLD.EPNC_NUMBER,','); 
END IF;
IF (OLD.EPNC_NUMBER!= NEW.EPNC_NUMBER) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EPNC_NUMBER=', NEW.EPNC_NUMBER,','); 
END IF;
IF (OLD.EPNC_INVOICE_DATE!= NEW.EPNC_INVOICE_DATE) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EPNC_INVOICE_DATE=', OLD.EPNC_INVOICE_DATE,','); 
END IF;
IF (OLD.EPNC_INVOICE_DATE!= NEW.EPNC_INVOICE_DATE) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EPNC_INVOICE_DATE=', NEW.EPNC_INVOICE_DATE,','); 
END IF;
IF (OLD.EPNC_AMOUNT!= NEW.EPNC_AMOUNT) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EPNC_AMOUNT=', OLD.EPNC_AMOUNT,','); 
END IF;
IF (OLD.EPNC_AMOUNT!= NEW.EPNC_AMOUNT) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EPNC_AMOUNT=', NEW.EPNC_AMOUNT,','); 
END IF;
IF (OLD.EPNC_COMMENTS IS NULL AND NEW.EPNC_COMMENTS IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EPNC_COMMENTS=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EPNC_COMMENTS=',NEW.EPNC_COMMENTS,',');
ELSEIF(OLD.EPNC_COMMENTS IS NOT NULL AND NEW.EPNC_COMMENTS IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EPNC_COMMENTS=',OLD.EPNC_COMMENTS,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EPNC_COMMENTS=','<NULL>,');
ELSEIF (OLD.EPNC_COMMENTS!= NEW.EPNC_COMMENTS) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'EPNC_COMMENTS=', OLD.EPNC_COMMENTS,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'EPNC_COMMENTS=', NEW.EPNC_COMMENTS,','); 
END IF;
IF (OLD.UNIT_ID!= NEW.UNIT_ID) OR (OLD.EPNC_NUMBER!= NEW.EPNC_NUMBER) OR (OLD.EPNC_INVOICE_DATE!= NEW.EPNC_INVOICE_DATE) OR  (OLD.EPNC_AMOUNT!= NEW.EPNC_AMOUNT)  OR (OLD.EPNC_COMMENTS IS NULL AND NEW.EPNC_COMMENTS IS NOT NULL) OR (OLD.EPNC_COMMENTS IS NOT NULL AND NEW.EPNC_COMMENTS IS NULL) OR (OLD.EPNC_COMMENTS != NEW.EPNC_COMMENTS) THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.EPNC_TIMESTAMP!= NEW.EPNC_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EPNC_TIMESTAMP=', OLD.EPNC_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF (OLD_VALUE != NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EXPENSE_PURCHASE_NEW_CARD  WHERE EPNC_ID=NEW.EPNC_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EXPENSE_PURCHASE_NEW_CARD '),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;
