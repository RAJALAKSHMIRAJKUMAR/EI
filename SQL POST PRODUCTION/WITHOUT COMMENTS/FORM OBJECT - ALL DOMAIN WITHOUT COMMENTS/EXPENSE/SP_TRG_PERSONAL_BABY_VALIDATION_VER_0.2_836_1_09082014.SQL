DROP PROCEDURE IF EXISTS SP_TRG_PERSONAL_BABY_VALIDATION;
CREATE PROCEDURE SP_TRG_PERSONAL_BABY_VALIDATION(
IN NEWECNID INTEGER,
IN NEWINVOICEDATE DATE,
IN PROCESS TEXT)
BEGIN
    DECLARE MESSAGE_TEXT VARCHAR(50);
	DECLARE ERROR_MSG TEXT;
	IF(PROCESS = 'INSERT') OR (PROCESS = 'UPDATE') THEN
        IF(NEWINVOICEDATE > CURDATE()) THEN
			SET ERROR_MSG = (SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID = 474);
        	SIGNAL SQLSTATE '45000'
        	SET MESSAGE_TEXT = ERROR_MSG;
        END IF;
        IF(NEWECNID IS NOT NULL) THEN
            IF NOT EXISTS(SELECT ECN_ID FROM EXPENSE_CONFIGURATION WHERE ECN_ID = NEWECNID AND CGN_ID = 25)THEN
				SET ERROR_MSG = (SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID = 550);
                SIGNAL SQLSTATE '45000'
                SET MESSAGE_TEXT = ERROR_MSG;
            END IF;
        END IF;
    END IF;
END;
