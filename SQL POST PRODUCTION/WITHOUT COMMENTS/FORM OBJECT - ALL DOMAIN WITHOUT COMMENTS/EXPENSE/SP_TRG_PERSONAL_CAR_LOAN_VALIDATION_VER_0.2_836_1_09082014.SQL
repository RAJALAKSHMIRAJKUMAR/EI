DROP PROCEDURE IF EXISTS SP_TRG_PERSONAL_CAR_LOAN_VALIDATION;
CREATE PROCEDURE SP_TRG_PERSONAL_CAR_LOAN_VALIDATION(
IN NEWECLPAIDDATE DATE,
IN NEWECLFRMPERIOD DATE,
IN NEWECLTOPERIOD DATE,
IN PROCESS TEXT)
BEGIN
	DECLARE MESSAGE_TEXT VARCHAR(50);
	DECLARE ERROR_MSG TEXT;
	IF(PROCESS = 'INSERT') OR (PROCESS='UPDATE')THEN
		IF(NEWECLPAIDDATE > CURDATE()) THEN
			SET ERROR_MSG = (SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID = 545);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERROR_MSG;
		END IF;
		IF(NEWECLFRMPERIOD > NEWECLPAIDDATE) THEN
			SET ERROR_MSG = (SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID = 546);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERROR_MSG;
		END IF;
		IF(NEWECLTOPERIOD NOT BETWEEN NEWECLFRMPERIOD AND NEWECLPAIDDATE) THEN
			SET ERROR_MSG = (SELECT EMC_DATA FROM ERROR_MESSAGE_CONFIGURATION WHERE EMC_ID = 547);
			SIGNAL SQLSTATE '45000'
			SET MESSAGE_TEXT = ERROR_MSG;
		END IF;
	END IF;
END;
