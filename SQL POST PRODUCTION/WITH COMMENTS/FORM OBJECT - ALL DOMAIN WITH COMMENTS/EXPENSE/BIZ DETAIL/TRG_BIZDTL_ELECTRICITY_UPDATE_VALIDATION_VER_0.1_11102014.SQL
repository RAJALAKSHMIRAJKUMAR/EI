DROP TRIGGER IF EXISTS TRG_BIZDTL_ELECTRICITY_UPDATE;
CREATE TRIGGER TRG_BIZDTL_ELECTRICITY_UPDATE  
AFTER UPDATE ON EXPENSE_DETAIL_ELECTRICITY
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.EDE_ID!= NEW.EDE_ID) OR (OLD.UNIT_ID!= NEW.UNIT_ID) OR (OLD.ECN_ID != NEW.ECN_ID ) OR  (OLD.EDE_REC_VER != NEW.EDE_REC_VER ) OR (OLD.EDE_COMMENTS IS NULL AND NEW.EDE_COMMENTS IS NOT NULL) OR (OLD.EDE_COMMENTS IS NOT NULL AND NEW.EDE_COMMENTS IS NULL) OR (OLD.EDE_COMMENTS != NEW.EDE_COMMENTS) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'EDE_ID=', OLD.EDE_ID,','); 
END IF;
IF (OLD.EDE_ID!= NEW.EDE_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDE_ID=', OLD.EDE_ID,','); 
END IF;
IF (OLD.EDE_ID!= NEW.EDE_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EDE_ID=', NEW.EDE_ID,','); 
END IF;
IF (OLD.UNIT_ID!= NEW.UNIT_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'UNIT_ID=', OLD.UNIT_ID,','); 
END IF;
IF (OLD.UNIT_ID!= NEW.UNIT_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'UNIT_ID=', NEW.UNIT_ID,','); 
END IF;
IF (OLD.ECN_ID!= NEW.ECN_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ECN_ID=', OLD.ECN_ID,','); 
END IF;
IF (OLD.ECN_ID!= NEW.ECN_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ECN_ID=', NEW.ECN_ID,','); 
END IF;
IF (OLD.EDE_REC_VER!= NEW.EDE_REC_VER) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDE_REC_VER=', OLD.EDE_REC_VER,','); 
END IF;
IF (OLD.EDE_REC_VER!= NEW.EDE_REC_VER) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EDE_REC_VER=', NEW.EDE_REC_VER,','); 
END IF;
IF (OLD.EDE_COMMENTS IS NULL AND NEW.EDE_COMMENTS IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EDE_COMMENTS=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EDE_COMMENTS=',NEW.EDE_COMMENTS,',');
ELSEIF(OLD.EDE_COMMENTS IS NOT NULL AND NEW.EDE_COMMENTS IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EDE_COMMENTS=',OLD.EDE_COMMENTS,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EDE_COMMENTS=','<NULL>,');
ELSEIF (OLD.EDE_COMMENTS!= NEW.EDE_COMMENTS) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDE_COMMENTS=', OLD.EDE_COMMENTS,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'EDE_COMMENTS=', NEW.EDE_COMMENTS,','); 
END IF;
IF (OLD.EDE_ID!= NEW.EDE_ID) OR (OLD.UNIT_ID!= NEW.UNIT_ID) OR (OLD.ECN_ID != NEW.ECN_ID ) OR  (OLD.EDE_REC_VER != NEW.EDE_REC_VER ) OR  (OLD.EDE_COMMENTS IS NULL AND NEW.EDE_COMMENTS IS NOT NULL) OR (OLD.EDE_COMMENTS IS NOT NULL AND NEW.EDE_COMMENTS IS NULL) OR (OLD.EDE_COMMENTS != NEW.EDE_COMMENTS) THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.EDE_TIMESTAMP!= NEW.EDE_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDE_TIMESTAMP=', OLD.EDE_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE!=NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EXPENSE_DETAIL_ELECTRICITY WHERE EDE_ID=NEW.EDE_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EXPENSE_DETAIL_ELECTRICITY'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;