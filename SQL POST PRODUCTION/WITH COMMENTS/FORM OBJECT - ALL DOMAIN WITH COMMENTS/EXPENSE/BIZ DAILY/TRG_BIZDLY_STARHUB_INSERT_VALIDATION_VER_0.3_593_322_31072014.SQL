-- TRIGGER FOR BEFORE INSERT VALIDATION

DROP TRIGGER IF EXISTS TRG_BIZDLY_STARHUB_INSERT_VALIDATION;
CREATE TRIGGER TRG_BIZDLY_STARHUB_INSERT_VALIDATION
BEFORE INSERT ON EXPENSE_STARHUB
FOR EACH ROW
BEGIN
