DROP PROCEDURE IF EXISTS SP_DROP_DYNAMIC_TEMP_TABLE;
CREATE PROCEDURE SP_DROP_DYNAMIC_TEMP_TABLE(IN TEMPTABLE TEXT)
BEGIN
-- DROP QUERY FOR DYNAMIC TEMP TABLE
SET @DROP_QUERY=(SELECT CONCAT('DROP TABLE IF EXISTS ',TEMPTABLE));
PREPARE DROP_QUERY_STMT FROM @DROP_QUERY;
EXECUTE DROP_QUERY_STMT;
END;
