	(TCHKTRS_ID INTEGER AUTO_INCREMENT,TCHKTRS_CUSTOMERID INTEGER,TCHKTRS_INVOICEDATE DATE,TCHKTRS_RECVER INTEGER,PRIMARY KEY(TCHKTRS_ID))'));
