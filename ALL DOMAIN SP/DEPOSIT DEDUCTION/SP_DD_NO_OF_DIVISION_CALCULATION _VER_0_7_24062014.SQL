	TDDAC_ID INTEGER AUTO_INCREMENT,TDDAC_UNITNO INTEGER,TDDAC_CUSTOMERID INTEGER,
	TDDAC_RECVER INTEGER,TDDAC_STARTDATE DATE,TDDAC_ENDDATE DATE,TDDAC_PTDDATE DATE,PRIMARY KEY(TDDAC_ID))'));
