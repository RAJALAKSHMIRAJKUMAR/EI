DROP TRIGGER IF EXISTS TRG_NATIONALITY_CONFIGURATION_UPDATE;
CREATE TRIGGER TRG_NATIONALITY_CONFIGURATION_UPDATE  
AFTER UPDATE ON NATIONALITY_CONFIGURATION
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.NC_DATA!= NEW.NC_DATA) OR (OLD.NC_INITIALIZE_FLAG IS NULL AND NEW.NC_INITIALIZE_FLAG IS NOT NULL) OR (OLD.NC_INITIALIZE_FLAG IS NOT NULL AND NEW.NC_INITIALIZE_FLAG IS NULL) OR (OLD.NC_INITIALIZE_FLAG!= NEW.NC_INITIALIZE_FLAG) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'NC_ID=', OLD.NC_ID,','); 
END IF;
IF (OLD.CGN_ID!= NEW.CGN_ID) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'CGN_ID=', OLD.CGN_ID,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'CGN_ID=', NEW.CGN_ID,','); 
END IF;
IF (OLD.NC_DATA!= NEW.NC_DATA) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'NC_DATA=', OLD.NC_DATA,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'NC_DATA=', NEW.NC_DATA,','); 
END IF;
IF (OLD.NC_INITIALIZE_FLAG IS NULL AND NEW.NC_INITIALIZE_FLAG IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'NC_INITIALIZE_FLAG=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'NC_INITIALIZE_FLAG=',NEW.NC_INITIALIZE_FLAG,',');
ELSEIF (OLD.NC_INITIALIZE_FLAG IS NOT NULL AND NEW.NC_INITIALIZE_FLAG IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'NC_INITIALIZE_FLAG=',OLD.NC_INITIALIZE_FLAG,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'NC_INITIALIZE_FLAG=','<NULL>,');
ELSEIF (OLD.NC_INITIALIZE_FLAG!= NEW.NC_INITIALIZE_FLAG) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'NC_INITIALIZE_FLAG=', OLD.NC_INITIALIZE_FLAG,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'NC_INITIALIZE_FLAG=', NEW.NC_INITIALIZE_FLAG,','); 
END IF;
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.NC_DATA!= NEW.NC_DATA) OR (OLD.NC_INITIALIZE_FLAG IS NULL AND NEW.NC_INITIALIZE_FLAG IS NOT NULL) OR (OLD.NC_INITIALIZE_FLAG IS NOT NULL AND NEW.NC_INITIALIZE_FLAG IS NULL) OR (OLD.NC_INITIALIZE_FLAG!= NEW.NC_INITIALIZE_FLAG) THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.NC_TIMESTAMP!= NEW.NC_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'NC_TIMESTAMP=', OLD.NC_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF (OLD_VALUE!=NEW_VALUE)THEN
     SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
     SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM NATIONALITY_CONFIGURATION WHERE NC_ID=NEW.NC_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='NATIONALITY_CONFIGURATION'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;