DROP TRIGGER IF EXISTS TRG_PAYMENT_CONFIGURATION_UPDATE;
CREATE TRIGGER TRG_PAYMENT_CONFIGURATION_UPDATE  
AFTER UPDATE ON PAYMENT_CONFIGURATION
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.PCN_DATA!= NEW.PCN_DATA) OR (OLD.PCN_INITIALIZE_FLAG IS NULL AND NEW.PCN_INITIALIZE_FLAG IS NOT NULL) OR (OLD.PCN_INITIALIZE_FLAG IS NOT NULL AND NEW.PCN_INITIALIZE_FLAG IS NULL) OR (OLD.PCN_INITIALIZE_FLAG!= NEW.PCN_INITIALIZE_FLAG) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'PCN_ID=', OLD.PCN_ID,','); 
END IF;
IF (OLD.CGN_ID!= NEW.CGN_ID) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'CGN_ID=', OLD.CGN_ID,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'CGN_ID=', NEW.CGN_ID,','); 
END IF;
IF (OLD.PCN_DATA!= NEW.PCN_DATA) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'PCN_DATA=', OLD.PCN_DATA,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'PCN_DATA=', NEW.PCN_DATA,','); 
END IF;
IF (OLD.PCN_INITIALIZE_FLAG IS NULL AND NEW.PCN_INITIALIZE_FLAG IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'PCN_INITIALIZE_FLAG=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'PCN_INITIALIZE_FLAG=',NEW.PCN_INITIALIZE_FLAG,',');
ELSEIF (OLD.PCN_INITIALIZE_FLAG IS NOT NULL AND NEW.PCN_INITIALIZE_FLAG IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'PCN_INITIALIZE_FLAG=',OLD.PCN_INITIALIZE_FLAG,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'PCN_INITIALIZE_FLAG=','<NULL>,');
ELSEIF (OLD.PCN_INITIALIZE_FLAG!= NEW.PCN_INITIALIZE_FLAG) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'PCN_INITIALIZE_FLAG=', OLD.PCN_INITIALIZE_FLAG,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'PCN_INITIALIZE_FLAG=', NEW.PCN_INITIALIZE_FLAG,','); 
END IF;
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.PCN_DATA!= NEW.PCN_DATA) OR (OLD.PCN_INITIALIZE_FLAG IS NULL AND NEW.PCN_INITIALIZE_FLAG IS NOT NULL) OR (OLD.PCN_INITIALIZE_FLAG IS NOT NULL AND NEW.PCN_INITIALIZE_FLAG IS NULL) OR (OLD.PCN_INITIALIZE_FLAG!= NEW.PCN_INITIALIZE_FLAG) THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.PCN_TIMESTAMP!= NEW.PCN_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'PCN_TIMESTAMP=', OLD.PCN_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF (OLD_VALUE!=NEW_VALUE)THEN
     SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
     SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM PAYMENT_CONFIGURATION WHERE PCN_ID=NEW.PCN_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='PAYMENT_CONFIGURATION'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;