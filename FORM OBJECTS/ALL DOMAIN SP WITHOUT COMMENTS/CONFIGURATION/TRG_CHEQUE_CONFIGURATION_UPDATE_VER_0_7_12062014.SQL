DROP TRIGGER IF EXISTS TRG_CHEQUE_CONFIGURATION_UPDATE ;
CREATE TRIGGER TRG_CHEQUE_CONFIGURATION_UPDATE   
AFTER UPDATE ON CHEQUE_CONFIGURATION
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.CQCN_DATA!= NEW.CQCN_DATA) OR (OLD.CQCN_INITIALIZE_FLAG IS NULL AND NEW.CQCN_INITIALIZE_FLAG IS NOT NULL) OR (OLD.CQCN_INITIALIZE_FLAG IS NOT NULL AND NEW.CQCN_INITIALIZE_FLAG IS NULL) OR (OLD.CQCN_INITIALIZE_FLAG!= NEW.CQCN_INITIALIZE_FLAG)  THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'CQCN_ID=', OLD.CQCN_ID,','); 
END IF;
IF (OLD.CGN_ID!= NEW.CGN_ID) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'CGN_ID=', OLD.CGN_ID,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'CGN_ID=', NEW.CGN_ID,','); 
END IF;
IF (OLD.CQCN_DATA!= NEW.CQCN_DATA) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'CQCN_DATA=', OLD.CQCN_DATA,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'CQCN_DATA=', NEW.CQCN_DATA,','); 
END IF;
IF (OLD.CQCN_INITIALIZE_FLAG IS NULL AND NEW.CQCN_INITIALIZE_FLAG IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'CQCN_INITIALIZE_FLAG=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'CQCN_INITIALIZE_FLAG=',NEW.CQCN_INITIALIZE_FLAG,',');
ELSEIF (OLD.CQCN_INITIALIZE_FLAG IS NOT NULL AND NEW.CQCN_INITIALIZE_FLAG IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'CQCN_INITIALIZE_FLAG=',OLD.CQCN_INITIALIZE_FLAG,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'CQCN_INITIALIZE_FLAG=','<NULL>,');
ELSEIF (OLD.CQCN_INITIALIZE_FLAG!= NEW.CQCN_INITIALIZE_FLAG) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'CQCN_INITIALIZE_FLAG=', OLD.CQCN_INITIALIZE_FLAG,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'CQCN_INITIALIZE_FLAG=', NEW.CQCN_INITIALIZE_FLAG,','); 
END IF;
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.CQCN_DATA!= NEW.CQCN_DATA) OR (OLD.CQCN_INITIALIZE_FLAG IS NULL AND NEW.CQCN_INITIALIZE_FLAG IS NOT NULL) OR (OLD.CQCN_INITIALIZE_FLAG IS NOT NULL AND NEW.CQCN_INITIALIZE_FLAG IS NULL) OR (OLD.CQCN_INITIALIZE_FLAG!= NEW.CQCN_INITIALIZE_FLAG)  THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.CQCN_TIMESTAMP!= NEW.CQCN_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'CQCN_TIMESTAMP=', OLD.CQCN_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF (OLD_VALUE!=NEW_VALUE)THEN
     SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
     SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM CHEQUE_CONFIGURATION WHERE CQCN_ID=NEW.CQCN_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='CHEQUE_CONFIGURATION'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;