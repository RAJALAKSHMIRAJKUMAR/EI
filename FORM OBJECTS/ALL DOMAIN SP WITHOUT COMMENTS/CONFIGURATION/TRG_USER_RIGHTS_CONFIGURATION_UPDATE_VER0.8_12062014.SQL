DROP TRIGGER IF EXISTS TRG_USER_RIGHTS_CONFIGURATION_UPDATE ;
CREATE TRIGGER TRG_USER_RIGHTS_CONFIGURATION_UPDATE   
AFTER UPDATE ON USER_RIGHTS_CONFIGURATION
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
 DECLARE ULDUSERSTAMP VARCHAR(50);
DECLARE ULDID INTEGER(2);
SET ULDID = (SELECT ULD_ID FROM USER_LOGIN_DETAILS WHERE ULD_LOGINID=NEW.URC_USERSTAMP);
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.URC_DATA!= NEW.URC_DATA) OR (OLD.URC_INITIALIZE_FLAG IS NULL AND NEW.URC_INITIALIZE_FLAG IS NOT NULL) OR (OLD.URC_INITIALIZE_FLAG IS NOT NULL AND NEW.URC_INITIALIZE_FLAG IS NULL) OR (OLD.URC_INITIALIZE_FLAG!= NEW.URC_INITIALIZE_FLAG) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'URC_ID=', OLD.URC_ID,','); 
END IF;
IF (OLD.CGN_ID!= NEW.CGN_ID) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'CGN_ID=', OLD.CGN_ID,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'CGN_ID=', NEW.CGN_ID,','); 
END IF;
IF (OLD.URC_DATA!= NEW.URC_DATA) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'URC_DATA=', OLD.URC_DATA,','); 
  SET NEW_VALUE = CONCAT(NEW_VALUE,'URC_DATA=', NEW.URC_DATA,',');
END IF;
IF (OLD.URC_INITIALIZE_FLAG IS NULL AND NEW.URC_INITIALIZE_FLAG IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'URC_INITIALIZE_FLAG=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'URC_INITIALIZE_FLAG=',NEW.URC_INITIALIZE_FLAG,',');
ELSEIF (OLD.URC_INITIALIZE_FLAG IS NOT NULL AND NEW.URC_INITIALIZE_FLAG IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'URC_INITIALIZE_FLAG=',OLD.URC_INITIALIZE_FLAG,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'URC_INITIALIZE_FLAG=','<NULL>,');
ELSEIF (OLD.URC_INITIALIZE_FLAG!= NEW.URC_INITIALIZE_FLAG) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'URC_INITIALIZE_FLAG=', OLD.URC_INITIALIZE_FLAG,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'URC_INITIALIZE_FLAG=', NEW.URC_INITIALIZE_FLAG,','); 
END IF;
IF (OLD.CGN_ID!= NEW.CGN_ID) OR (OLD.URC_DATA!= NEW.URC_DATA) OR (OLD.URC_INITIALIZE_FLAG IS NULL AND NEW.URC_INITIALIZE_FLAG IS NOT NULL) OR (OLD.URC_INITIALIZE_FLAG IS NOT NULL AND NEW.URC_INITIALIZE_FLAG IS NULL) OR (OLD.URC_INITIALIZE_FLAG!= NEW.URC_INITIALIZE_FLAG) THEN  
IF (OLD.URC_USERSTAMP!= NEW.URC_USERSTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'URC_USERSTAMP=', OLD.URC_USERSTAMP,','); 
END IF;
IF (OLD.URC_TIMESTAMP!= NEW.URC_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'URC_TIMESTAMP=', OLD.URC_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE!=NEW_VALUE)THEN
     SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
     SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
(ULDID,
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='USER_RIGHTS_CONFIGURATION'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;