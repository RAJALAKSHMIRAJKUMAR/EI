DROP TRIGGER IF EXISTS TRG_BIZDLY_PETTY_CASH_UPDATE;
CREATE TRIGGER TRG_BIZDLY_PETTY_CASH_UPDATE  
AFTER UPDATE ON EXPENSE_PETTY_CASH 
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.EPC_DATE!= NEW.EPC_DATE) OR (OLD.EPC_CASH_IN IS NULL AND NEW.EPC_CASH_IN IS NOT NULL) OR  (OLD.EPC_CASH_IN IS NOT NULL AND NEW.EPC_CASH_IN IS NULL) OR (OLD.EPC_CASH_IN!= NEW.EPC_CASH_IN) OR (OLD.EPC_CASH_OUT IS NULL AND NEW.EPC_CASH_OUT IS NOT NULL) OR (OLD.EPC_CASH_OUT IS NOT NULL AND NEW.EPC_CASH_OUT IS NULL) OR (OLD.EPC_CASH_OUT!= NEW.EPC_CASH_OUT) OR (OLD.EPC_BALANCE!= NEW.EPC_BALANCE) OR (OLD.EPC_INVOICE_ITEMS!= NEW.EPC_INVOICE_ITEMS) OR (OLD.EPC_COMMENTS IS NULL AND NEW.EPC_COMMENTS IS NOT NULL) OR (OLD.EPC_COMMENTS IS NOT NULL AND NEW.EPC_COMMENTS IS NULL) OR (OLD.EPC_COMMENTS != NEW.EPC_COMMENTS) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'EPC_ID=', OLD.EPC_ID,','); 
END IF;
IF (OLD.EPC_DATE!= NEW.EPC_DATE) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EPC_DATE=', OLD.EPC_DATE,','); 
END IF;
IF (OLD.EPC_DATE!= NEW.EPC_DATE) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EPC_DATE=', NEW.EPC_DATE,','); 
END IF;
IF (OLD.EPC_CASH_IN IS NULL AND NEW.EPC_CASH_IN IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EPC_CASH_IN=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EPC_CASH_IN=',NEW.EPC_CASH_IN,',');
ELSEIF(OLD.EPC_CASH_IN IS NOT NULL AND NEW.EPC_CASH_IN IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EPC_CASH_IN=',OLD.EPC_CASH_IN,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EPC_CASH_IN=','<NULL>,');
ELSEIF (OLD.EPC_CASH_IN!= NEW.EPC_CASH_IN) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'EPC_CASH_IN=', OLD.EPC_CASH_IN,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'EPC_CASH_IN=', NEW.EPC_CASH_IN,','); 
END IF;
IF (OLD.EPC_CASH_OUT IS NULL AND NEW.EPC_CASH_OUT IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EPC_CASH_OUT=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EPC_CASH_OUT=',NEW.EPC_CASH_OUT,',');
ELSEIF(OLD.EPC_CASH_OUT IS NOT NULL AND NEW.EPC_CASH_OUT IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EPC_CASH_OUT=',OLD.EPC_CASH_OUT,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EPC_CASH_OUT=','<NULL>,');
ELSEIF (OLD.EPC_CASH_OUT!= NEW.EPC_CASH_OUT) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'EPC_CASH_OUT=', OLD.EPC_CASH_OUT,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'EPC_CASH_OUT=', NEW.EPC_CASH_OUT,','); 
END IF;
IF (OLD.EPC_BALANCE!= NEW.EPC_BALANCE) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EPC_BALANCE=', OLD.EPC_BALANCE,','); 
END IF;
IF (OLD.EPC_BALANCE!= NEW.EPC_BALANCE) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EPC_BALANCE=', NEW.EPC_BALANCE,','); 
END IF;
IF (OLD.EPC_INVOICE_ITEMS!= NEW.EPC_INVOICE_ITEMS) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EPC_INVOICE_ITEMS=', OLD.EPC_INVOICE_ITEMS,','); 
END IF;
IF (OLD.EPC_INVOICE_ITEMS!= NEW.EPC_INVOICE_ITEMS) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EPC_INVOICE_ITEMS=', NEW.EPC_INVOICE_ITEMS,','); 
END IF;
IF (OLD.EPC_COMMENTS IS NULL AND NEW.EPC_COMMENTS IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EPC_COMMENTS=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EPC_COMMENTS=',NEW.EPC_COMMENTS,',');
ELSEIF(OLD.EPC_COMMENTS IS NOT NULL AND NEW.EPC_COMMENTS IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'EPC_COMMENTS=',OLD.EPC_COMMENTS,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'EPC_COMMENTS=','<NULL>,');
ELSEIF (OLD.EPC_COMMENTS!= NEW.EPC_COMMENTS) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'EPC_COMMENTS=', OLD.EPC_COMMENTS,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'EPC_COMMENTS=', NEW.EPC_COMMENTS,','); 
END IF;
IF (OLD.EPC_DATE!= NEW.EPC_DATE) OR (OLD.EPC_CASH_IN IS NULL AND NEW.EPC_CASH_IN IS NOT NULL) OR  (OLD.EPC_CASH_IN IS NOT NULL AND NEW.EPC_CASH_IN IS NULL) OR (OLD.EPC_CASH_IN!= NEW.EPC_CASH_IN) OR (OLD.EPC_CASH_OUT IS NULL AND NEW.EPC_CASH_OUT IS NOT NULL) OR (OLD.EPC_CASH_OUT IS NOT NULL AND NEW.EPC_CASH_OUT IS NULL) OR (OLD.EPC_CASH_OUT!= NEW.EPC_CASH_OUT) OR (OLD.EPC_BALANCE!= NEW.EPC_BALANCE) OR (OLD.EPC_INVOICE_ITEMS!= NEW.EPC_INVOICE_ITEMS) OR (OLD.EPC_COMMENTS IS NULL AND NEW.EPC_COMMENTS IS NOT NULL) OR (OLD.EPC_COMMENTS IS NOT NULL AND NEW.EPC_COMMENTS IS NULL) OR (OLD.EPC_COMMENTS != NEW.EPC_COMMENTS) THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.EPC_TIMESTAMP!= NEW.EPC_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EPC_TIMESTAMP=', OLD.EPC_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE != NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EXPENSE_PETTY_CASH  WHERE EPC_ID=NEW.EPC_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EXPENSE_PETTY_CASH '),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;