DROP TRIGGER IF EXISTS TRG_BIZDLY_HOUSEKEEPING_UPDATE;
CREATE TRIGGER TRG_BIZDLY_HOUSEKEEPING_UPDATE  
AFTER UPDATE ON EXPENSE_HOUSEKEEPING 
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.EMP_ID!= NEW.EMP_ID) OR (OLD.EHK_WORK_DATE!= NEW.EHK_WORK_DATE) OR (OLD.EHK_DURATION != NEW.EHK_DURATION ) OR  (OLD.EHK_DESCRIPTION != NEW.EHK_DESCRIPTION ) THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'EHK_ID=', OLD.EHK_ID,','); 
END IF;
IF (OLD.EMP_ID!= NEW.EMP_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EMP_ID=', OLD.EMP_ID,','); 
END IF;
IF (OLD.EMP_ID!= NEW.EMP_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EMP_ID=', NEW.EMP_ID,','); 
END IF;
IF (OLD.EHK_WORK_DATE!= NEW.EHK_WORK_DATE) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EHK_WORK_DATE=', OLD.EHK_WORK_DATE,','); 
END IF;
IF (OLD.EHK_WORK_DATE!= NEW.EHK_WORK_DATE) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EHK_WORK_DATE=', NEW.EHK_WORK_DATE,','); 
END IF;
IF (OLD.EHK_DURATION!= NEW.EHK_DURATION) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EHK_DURATION =', OLD.EHK_DURATION ,','); 
END IF;
IF (OLD.EHK_DURATION!= NEW.EHK_DURATION) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EHK_DURATION =', NEW.EHK_DURATION ,','); 
END IF;
IF (OLD.EHK_DESCRIPTION!= NEW.EHK_DESCRIPTION) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EHK_DESCRIPTION  =', OLD.EHK_DESCRIPTION  ,','); 
END IF;
IF (OLD.EHK_DESCRIPTION != NEW.EHK_DESCRIPTION) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EHK_DESCRIPTION  =', NEW.EHK_DESCRIPTION  ,','); 
END IF;
IF (OLD.EMP_ID!= NEW.EMP_ID) OR (OLD.EHK_WORK_DATE!= NEW.EHK_WORK_DATE) OR (OLD.EHK_DURATION != NEW.EHK_DURATION ) OR  (OLD.EHK_DESCRIPTION != NEW.EHK_DESCRIPTION ) THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.EHK_TIMESTAMP!= NEW.EHK_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EHK_TIMESTAMP=', OLD.EHK_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE != NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EXPENSE_HOUSEKEEPING  WHERE EHK_ID=NEW.EHK_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EXPENSE_HOUSEKEEPING'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;