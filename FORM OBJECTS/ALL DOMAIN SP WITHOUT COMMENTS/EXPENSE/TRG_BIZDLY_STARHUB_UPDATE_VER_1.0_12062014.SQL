DROP TRIGGER IF EXISTS TRG_BIZDLY_STARHUB_UPDATE;
CREATE TRIGGER TRG_BIZDLY_STARHUB_UPDATE  
AFTER UPDATE ON EXPENSE_STARHUB
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.EDSH_ID!= NEW.EDSH_ID) OR (OLD.ESH_INVOICE_DATE!= NEW.ESH_INVOICE_DATE) OR (OLD.ESH_FROM_PERIOD != NEW.ESH_FROM_PERIOD ) OR  (OLD.ESH_TO_PERIOD != NEW.ESH_TO_PERIOD ) OR (OLD.ESH_AMOUNT!= NEW.ESH_AMOUNT) OR (OLD.ESH_COMMENTS IS NULL AND NEW.ESH_COMMENTS IS NOT NULL) OR (OLD.ESH_COMMENTS IS NOT NULL AND NEW.ESH_COMMENTS IS NULL) OR (OLD.ESH_COMMENTS != NEW.ESH_COMMENTS)  THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'ESH_ID=', OLD.ESH_ID,','); 
END IF;
IF (OLD.EDSH_ID!= NEW.EDSH_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EDSH_ID=', OLD.EDSH_ID,','); 
END IF;
IF (OLD.EDSH_ID!= NEW.EDSH_ID) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EDSH_ID=', NEW.EDSH_ID,','); 
END IF;
IF (OLD.ESH_INVOICE_DATE!= NEW.ESH_INVOICE_DATE) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ESH_INVOICE_DATE=', OLD.ESH_INVOICE_DATE,','); 
END IF;
IF (OLD.ESH_INVOICE_DATE!= NEW.ESH_INVOICE_DATE) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ESH_INVOICE_DATE=', NEW.ESH_INVOICE_DATE,','); 
END IF;
IF (OLD.ESH_FROM_PERIOD!= NEW.ESH_FROM_PERIOD) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ESH_FROM_PERIOD=', OLD.ESH_FROM_PERIOD,','); 
END IF;
IF (OLD.ESH_FROM_PERIOD!= NEW.ESH_FROM_PERIOD) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ESH_FROM_PERIOD=', NEW.ESH_FROM_PERIOD,','); 
END IF;
IF (OLD.ESH_TO_PERIOD!= NEW.ESH_TO_PERIOD) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ESH_TO_PERIOD=', OLD.ESH_TO_PERIOD,','); 
END IF;
IF (OLD.ESH_TO_PERIOD!= NEW.ESH_TO_PERIOD) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ESH_TO_PERIOD=', NEW.ESH_TO_PERIOD,','); 
END IF;
IF (OLD.ESH_AMOUNT!= NEW.ESH_AMOUNT) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ESH_AMOUNT=', OLD.ESH_AMOUNT,','); 
END IF;
IF (OLD.ESH_AMOUNT!= NEW.ESH_AMOUNT) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'ESH_AMOUNT=', NEW.ESH_AMOUNT,','); 
END IF;
IF (OLD.ESH_COMMENTS IS NULL AND NEW.ESH_COMMENTS IS NOT NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ESH_COMMENTS=','<NULL>,');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ESH_COMMENTS=',NEW.ESH_COMMENTS,',');
ELSEIF(OLD.ESH_COMMENTS IS NOT NULL AND NEW.ESH_COMMENTS IS NULL) THEN
SET OLD_VALUE=CONCAT(OLD_VALUE,'ESH_COMMENTS=',OLD.ESH_COMMENTS,',');
SET NEW_VALUE=CONCAT(NEW_VALUE,'ESH_COMMENTS=','<NULL>,');
ELSEIF (OLD.ESH_COMMENTS!= NEW.ESH_COMMENTS) THEN 
SET OLD_VALUE = CONCAT(OLD_VALUE,'ESH_COMMENTS=', OLD.ESH_COMMENTS,','); 
SET NEW_VALUE = CONCAT(NEW_VALUE,'ESH_COMMENTS=', NEW.ESH_COMMENTS,','); 
END IF;
IF (OLD.EDSH_ID!= NEW.EDSH_ID) OR (OLD.ESH_INVOICE_DATE!= NEW.ESH_INVOICE_DATE) OR (OLD.ESH_FROM_PERIOD != NEW.ESH_FROM_PERIOD ) OR  (OLD.ESH_TO_PERIOD != NEW.ESH_TO_PERIOD ) OR (OLD.ESH_AMOUNT!= NEW.ESH_AMOUNT) OR (OLD.ESH_COMMENTS IS NULL AND NEW.ESH_COMMENTS IS NOT NULL) OR (OLD.ESH_COMMENTS IS NOT NULL AND NEW.ESH_COMMENTS IS NULL) OR (OLD.ESH_COMMENTS != NEW.ESH_COMMENTS)  THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.ESH_TIMESTAMP!= NEW.ESH_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ESH_TIMESTAMP=', OLD.ESH_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE != NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EXPENSE_STARHUB WHERE ESH_ID=NEW.ESH_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EXPENSE_STARHUB'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;