DROP TRIGGER IF EXISTS TRG_BIZDLY_HOUSEKEEPING_UNIT_UPDATE;
CREATE TRIGGER TRG_BIZDLY_HOUSEKEEPING_UNIT_UPDATE  
AFTER UPDATE ON EXPENSE_HOUSEKEEPING_UNIT 
FOR EACH ROW
BEGIN 
DECLARE OLD_VALUE TEXT DEFAULT '';
DECLARE NEW_VALUE TEXT DEFAULT '';
IF (OLD.EHKU_UNIT_NO!= NEW.EHKU_UNIT_NO)  THEN  
  SET OLD_VALUE = CONCAT(OLD_VALUE,'EHKU_ID=', OLD.EHKU_ID,','); 
END IF;
IF (OLD.EHKU_UNIT_NO!= NEW.EHKU_UNIT_NO) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EHKU_UNIT_NO=', OLD.EHKU_UNIT_NO,','); 
END IF;
IF (OLD.EHKU_UNIT_NO!= NEW.EHKU_UNIT_NO) THEN SET 
NEW_VALUE = CONCAT(NEW_VALUE,'EHKU_UNIT_NO=', NEW.EHKU_UNIT_NO,','); 
END IF;
IF (OLD.EHKU_UNIT_NO!= NEW.EHKU_UNIT_NO)  THEN  
IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
END IF;
IF (OLD.EHKU_TIMESTAMP!= NEW.EHKU_TIMESTAMP) THEN SET 
OLD_VALUE = CONCAT(OLD_VALUE,'EHKU_TIMESTAMP=', OLD.EHKU_TIMESTAMP,','); 
END IF;
END IF;
IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
IF(OLD_VALUE != NEW_VALUE)THEN
SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
INSERT INTO TICKLER_HISTORY
(ULD_ID,TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE)VALUES
((SELECT ULD_ID FROM EXPENSE_HOUSEKEEPING_UNIT  WHERE EHKU_ID=NEW.EHKU_ID),
(SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='EXPENSE_HOUSEKEEPING_UNIT'),OLD_VALUE,NEW_VALUE);
END IF;
END IF;
END;