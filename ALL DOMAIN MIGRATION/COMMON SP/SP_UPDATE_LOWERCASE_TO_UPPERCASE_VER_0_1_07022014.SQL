DROP PROCEDURE IF EXISTS SP_UPDATE_LOWERCASE_TO_UPPERCASE;
CREATE PROCEDURE SP_UPDATE_LOWERCASE_TO_UPPERCASE()
BEGIN
--UNIT_ROOM_TYPE_DETAILS
	UPDATE UNIT_ROOM_TYPE_DETAILS SET URTD_ROOM_TYPE = UCASE(URTD_ROOM_TYPE);
--UNIT_STAMP_DUTY_TYPE
	UPDATE UNIT_STAMP_DUTY_TYPE SET USDT_DATA = UCASE(USDT_DATA);
--UNIT_LOGIN_DETAILS
	UPDATE UNIT_LOGIN_DETAILS SET ULD_WEBLOGIN = UCASE(ULD_WEBLOGIN);
--UNIT_ACCOUNT_DETAILS
	UPDATE UNIT_ACCOUNT_DETAILS SET UACD_ACC_NAME = UCASE(UACD_ACC_NAME) , UACD_BANK_ADDRESS = UCASE(UACD_BANK_ADDRESS);
--CUSTOMER
	UPDATE CUSTOMER SET CUSTOMER_FIRST_NAME = UCASE(CUSTOMER_FIRST_NAME) , CUSTOMER_LAST_NAME = UCASE(CUSTOMER_LAST_NAME);
--CUSTOMER_COMPANY_DETAILS
	UPDATE CUSTOMER_COMPANY_DETAILS SET CCD_COMPANY_NAME = UCASE(CCD_COMPANY_NAME) , CCD_COMPANY_ADDR = UCASE(CCD_COMPANY_ADDR);
--CUSTOMER_PERSONAL_DETAILS
	UPDATE CUSTOMER_PERSONAL_DETAILS SET CPD_EP_NO = UCASE(CPD_EP_NO) , CPD_PASSPORT_NO = UCASE(CPD_PASSPORT_NO);
--EXPENSE_AIRCON_SERVICE_BY
	UPDATE EXPENSE_AIRCON_SERVICE_BY SET EASB_DATA = UCASE(EASB_DATA);
--EXPENSE_DETAIL_STARHUB
	UPDATE EXPENSE_DETAIL_STARHUB SET EDSH_ACCOUNT_NO = UCASE(EDSH_ACCOUNT_NO) , EDSH_CABLE_BOX_SERIAL_NO = UCASE(EDSH_CABLE_BOX_SERIAL_NO) ,
	EDSH_MODEM_SERIAL_NO = UCASE(EDSH_MODEM_SERIAL_NO);
--EXPENSE_DETAIL_DIGITAL_VOICE
	UPDATE EXPENSE_DETAIL_DIGITAL_VOICE SET EDDV_DIGITAL_ACCOUNT_NO = UCASE(EDDV_DIGITAL_ACCOUNT_NO);
--EXPENSE_DETAIL_CARPARK
	UPDATE EXPENSE_DETAIL_CARPARK SET EDCP_CAR_NO = UCASE(EDCP_CAR_NO);
--EXPENSE_UNIT
	UPDATE EXPENSE_UNIT SET EU_INVOICE_FROM = UCASE(EU_INVOICE_FROM);
--EXPENSE_PERSONAL
	UPDATE EXPENSE_PERSONAL SET EP_INVOICE_FROM = UCASE(EP_INVOICE_FROM);
--EXPENSE_CAR
	UPDATE EXPENSE_CAR SET EC_INVOICE_FROM = UCASE(EC_INVOICE_FROM);
--EXPENSE_BABY
	UPDATE EXPENSE_BABY SET EB_INVOICE_FROM = UCASE(EB_INVOICE_FROM);
--BANK_TRANSFER
	UPDATE BANK_TRANSFER SET BT_BANK_ADDRESS = UCASE(BT_BANK_ADDRESS) , BT_CUST_REF = UCASE(BT_CUST_REF) , BT_ACC_NAME = UCASE(BT_ACC_NAME);
--BANK_TRANSFER_MODELS
	UPDATE BANK_TRANSFER_MODELS SET BTM_DATA = UCASE(BTM_DATA);
--CHEQUE_ENTRY_DETAILS
	UPDATE CHEQUE_ENTRY_DETAILS SET CHEQUE_TO = UCASE(CHEQUE_TO) , CHEQUE_FOR = UCASE(CHEQUE_FOR);
--ERM_ENTRY_DETAILS 
	UPDATE ERM_ENTRY_DETAILS SET ERM_CUST_NAME  = UCASE(ERM_CUST_NAME );
--EMPLOYEE_DETAILS
	UPDATE EMPLOYEE_DETAILS SET EMP_FIRST_NAME = UCASE(EMP_FIRST_NAME) , EMP_LAST_NAME = UCASE(EMP_LAST_NAME);
END;