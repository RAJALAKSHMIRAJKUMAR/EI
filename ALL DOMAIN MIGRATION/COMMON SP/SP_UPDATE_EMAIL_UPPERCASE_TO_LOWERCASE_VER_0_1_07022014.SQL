DROP PROCEDURE IF EXISTS SP_UPDATE_EMAIL_UPPERCASE_TO_LOWERCASE;
CREATE PROCEDURE SP_UPDATE_EMAIL_UPPERCASE_TO_LOWERCASE()
BEGIN
--CUSTOMER_PERSONAL_DETAILS
	UPDATE CUSTOMER_PERSONAL_DETAILS SET CPD_EMAIL = LCASE (CPD_EMAIL);
--EMAIL_LIST
	UPDATE EMAIL_LIST SET EL_EMAIL_ID = LCASE (EL_EMAIL_ID);
--EMPLOYEE_DETAILS
	UPDATE EMPLOYEE_DETAILS SET EMP_EMAIL = LCASE (EMP_EMAIL);
--USER_LOGIN_DETAILS
	UPDATE USER_LOGIN_DETAILS SET ULD_LOGINID = LCASE (ULD_LOGINID);
--ERM_ENTRY_DETAILS
	UPDATE ERM_ENTRY_DETAILS SET ERM_EMAIL_ID = LCASE (ERM_EMAIL_ID);
END;

CALL SP_UPDATE_EMAIL_UPPERCASE_TO_LOWERCASE();